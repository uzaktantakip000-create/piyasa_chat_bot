export const FALLBACK_LOCALE = "tr";

export function registerTranslations(locale, entries) {
  if (!locale || typeof entries !== "object" || entries === null) {
    return;
  }
  const normalized = String(locale).toLowerCase();
  if (!TRANSLATIONS[normalized]) {
    TRANSLATIONS[normalized] = {};
  }
  Object.assign(TRANSLATIONS[normalized], entries);
}

export function translate(locale, key) {
  const dictionary = TRANSLATIONS[locale] || TRANSLATIONS[FALLBACK_LOCALE] || {};
  if (dictionary[key]) {
    return dictionary[key];
  }
  const fallbackDictionary = TRANSLATIONS[FALLBACK_LOCALE] || {};
  return fallbackDictionary[key] || "";
}
